CREATE OR REPLACE VIEW MANUFACTURING_MAINTENANCE.SITEWISE.VW_ASSET_HEALTH AS
SELECT
    ph.PLANT_NAME,
    ph.PRODUCTION_LINE,
    ph.EQUIPMENT_ID,
    ph.EQUIPMENT_TYPE,
    ph.SITEWISE_ASSET_NAME,
    eh.NAME AS EQUIPMENT_NAME,
    eh.HEALTH_SCORE,
    eh.STATUS,
    eh.CRITICALITY,
    eh.CRITICAL_SENSORS,
    eh.WARNING_SENSORS,
    eh.LAST_MAINTENANCE
FROM MANUFACTURING_MAINTENANCE.SITEWISE.PLANT_HIERARCHY ph
JOIN MANUFACTURING_MAINTENANCE.CURATED.EQUIPMENT_HEALTH eh
    ON ph.EQUIPMENT_ID = eh.EQUIPMENT_ID;

TRUNCATE TABLE MANUFACTURING_MAINTENANCE.SITEWISE.ASSET_MODEL;
INSERT INTO MANUFACTURING_MAINTENANCE.SITEWISE.ASSET_MODEL
SELECT
    v.EQUIPMENT_ID AS ASSET_ID,
    v.EQUIPMENT_NAME AS ASSET_NAME,
    'mfg-maintenance/cranes' AS ASSET_MODEL_ID,
    v.PRODUCTION_LINE AS PARENT_ASSET,
    OBJECT_CONSTRUCT(
        'health_score', v.HEALTH_SCORE,
        'critical_sensors', v.CRITICAL_SENSORS,
        'warning_sensors', v.WARNING_SENSORS,
        'criticality', v.CRITICALITY,
        'plant', v.PLANT_NAME,
        'last_maintenance', v.LAST_MAINTENANCE::STRING
    ) AS PROPERTIES,
    CURRENT_TIMESTAMP() AS CREATED_AT
FROM MANUFACTURING_MAINTENANCE.SITEWISE.VW_ASSET_HEALTH v;

CREATE OR REPLACE VIEW MANUFACTURING_MAINTENANCE.SITEWISE.VW_SENSOR_ROLLUP AS
SELECT
    ph.PLANT_NAME,
    ph.PRODUCTION_LINE,
    am.ASSET_ID,
    am.ASSET_NAME,
    eh.HEALTH_SCORE,
    eh.CRITICAL_SENSORS,
    eh.WARNING_SENSORS,
    eh.STATUS,
    eh.CRITICALITY,
    aa.SENSOR_TYPE AS ANOMALY_TYPE,
    aa.TREND_DIRECTION AS SEVERITY,
    aa.CURRENT_VALUE AS SENSOR_VALUE,
    aa.THRESHOLD_HIGH AS THRESHOLD,
    aa.HOURS_TO_BREACH_ESTIMATE AS HOURS_TO_BREACH
FROM MANUFACTURING_MAINTENANCE.SITEWISE.ASSET_MODEL am
JOIN MANUFACTURING_MAINTENANCE.SITEWISE.PLANT_HIERARCHY ph ON am.ASSET_ID = ph.EQUIPMENT_ID
JOIN MANUFACTURING_MAINTENANCE.CURATED.EQUIPMENT_HEALTH eh ON am.ASSET_ID = eh.EQUIPMENT_ID
LEFT JOIN MANUFACTURING_MAINTENANCE.CURATED.ANOMALY_ALERTS aa ON aa.EQUIPMENT_ID = am.ASSET_ID;
