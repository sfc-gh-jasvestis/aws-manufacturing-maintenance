-- Expanded IoT SiteWise asset model: Plant -> Line -> Equipment hierarchy
-- ============================================================================
-- Architecture:
--   AWS IoT SiteWise model `mfg-maintenance-cranes` has 100+ assets
--   This script mirrors the hierarchy in Snowflake for analytics
-- ============================================================================

CREATE SCHEMA IF NOT EXISTS MANUFACTURING_MAINTENANCE.SITEWISE;

CREATE OR REPLACE TABLE MANUFACTURING_MAINTENANCE.SITEWISE.PLANT_HIERARCHY (
    PLANT_NAME VARCHAR,
    PRODUCTION_LINE VARCHAR,
    EQUIPMENT_ID VARCHAR,
    EQUIPMENT_TYPE VARCHAR,
    SITEWISE_ASSET_NAME VARCHAR,
    INSTALLED_DATE DATE
);

INSERT INTO MANUFACTURING_MAINTENANCE.SITEWISE.PLANT_HIERARCHY
WITH plants AS (
    SELECT 'Singapore Jurong' AS PLANT_NAME, 'Line-A1' AS LINE UNION ALL
    SELECT 'Singapore Jurong', 'Line-A2' UNION ALL
    SELECT 'Singapore Jurong', 'Line-A3' UNION ALL
    SELECT 'Singapore Tuas', 'Line-B1' UNION ALL
    SELECT 'Singapore Tuas', 'Line-B2' UNION ALL
    SELECT 'Singapore Tuas', 'Line-B3' UNION ALL
    SELECT 'Batam', 'Line-C1' UNION ALL
    SELECT 'Batam', 'Line-C2'
),
lines_numbered AS (
    SELECT PLANT_NAME, LINE, ROW_NUMBER() OVER (ORDER BY LINE) - 1 AS LINE_IDX
    FROM plants
),
equip AS (
    SELECT EQUIPMENT_ID, TYPE AS EQUIPMENT_TYPE, ROW_NUMBER() OVER (ORDER BY EQUIPMENT_ID) - 1 AS EQUIP_IDX
    FROM MANUFACTURING_MAINTENANCE.RAW.EQUIPMENT
)
SELECT
    l.PLANT_NAME,
    l.LINE AS PRODUCTION_LINE,
    e.EQUIPMENT_ID,
    e.EQUIPMENT_TYPE,
    e.EQUIPMENT_ID || '-' || REPLACE(e.EQUIPMENT_TYPE, '_', '-') AS SITEWISE_ASSET_NAME,
    DATEADD('day', -UNIFORM(365, 1825, RANDOM()), CURRENT_DATE()) AS INSTALLED_DATE
FROM equip e
JOIN lines_numbered l ON MOD(e.EQUIP_IDX, 8) = l.LINE_IDX;

CREATE OR REPLACE TABLE MANUFACTURING_MAINTENANCE.SITEWISE.PROPERTY_DEFINITIONS (
    PROPERTY_NAME VARCHAR,
    UNIT VARCHAR,
    DATA_TYPE VARCHAR,
    ALARM_LOW NUMBER(8,2),
    ALARM_HIGH NUMBER(8,2),
    DESCRIPTION VARCHAR
);

